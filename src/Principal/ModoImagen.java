 private boolean Flag = false;
 
    private void btnWebCamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnWebCamActionPerformed

        
        if(Estudiantes!=null){
              this.Flag = true;
              Dispositivos Pic = new Dispositivos(Estudiantes, false);
              Pic.setEstudiantes(Estudiantes);
              Pic.setVisible(true);   
              this.dispose();
        }
        
        if(Trabajadores!=null){
              this.Flag = true;
              Dispositivos Pic = new Dispositivos(Trabajadores, false);
              Pic.setTrabajadores(Trabajadores);
              Pic.setVisible(true);
              this.dispose();
        }
                   
        
// TODO add your handling code here:
    }//GEN-LAST:event_btnWebCamActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        if(Estudiantes!=null){   
        this.Flag = true;
        Estudiantes.AddPic();
        Estudiantes.setVisible(true);
        this.dispose();  
        }
        if(Trabajadores!=null){
        this.Flag = true;
        Trabajadores.AddPic();
        Trabajadores.setVisible(true);
        this.dispose();  
        }
                   
              // TODO add your handling code here:
    }//GEN-LAST:event_btnBuscarActionPerformed

